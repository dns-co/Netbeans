package praktikum_pbo3;
public class Titik {
   private double x, y;
   
  public Titik (double absis, double ordinat) {//constructor Titik
     x = absis;
     y = ordinat;
}	
public void setX (double absis) {
     x= absis;
}
public void setY(double ordinat) {
     x= ordinat;
}
public double getX (){
   return x;
}
public double getY (){
   return y;
}
}
 

